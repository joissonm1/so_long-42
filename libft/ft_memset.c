#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*ptr_s;

	ptr_s = (char *)s;
	while (n > 0)
	{
		ptr_s[n - 1] = c;
		n--;
	}
	return (s);
}
